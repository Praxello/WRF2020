package com.praxello.smartevent.model.advertisment;

import androidx.annotation.NonNull;

public class AdvertismentData {

    public String adId;
    public String adTitle;
    public String adDetails;
    public String adOwnerId;
    public String adType;
    public String adVideourl;
    public String adImageUrl;
    public String isActive;

    public String getAdId() {
        return adId;
    }

    public void setAdId(String adId) {
        this.adId = adId;
    }

    public String getAdTitle() {
        return adTitle;
    }

    public void setAdTitle(String adTitle) {
        this.adTitle = adTitle;
    }

    public String getAdDetails() {
        return adDetails;
    }

    public void setAdDetails(String adDetails) {
        this.adDetails = adDetails;
    }

    public String getAdOwnerId() {
        return adOwnerId;
    }

    public void setAdOwnerId(String adOwnerId) {
        this.adOwnerId = adOwnerId;
    }

    public String getAdType() {
        return adType;
    }

    public void setAdType(String adType) {
        this.adType = adType;
    }

    public String getAdVideourl() {
        return adVideourl;
    }

    public void setAdVideourl(String adVideourl) {
        this.adVideourl = adVideourl;
    }

    public String getAdImageUrl() {
        return adImageUrl;
    }

    public void setAdImageUrl(String adImageUrl) {
        this.adImageUrl = adImageUrl;
    }

    public String getIsActive() {
        return isActive;
    }

    public void setIsActive(String isActive) {
        this.isActive = isActive;
    }

    @NonNull
    @Override
    public String toString() {
        return super.toString();
    }
}
